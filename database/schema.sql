-- ============================================================
-- 11. TRIGGERS: Auto-update UserStats on AC submission
-- ============================================================

CREATE OR REPLACE TRIGGER trg_update_stats
AFTER INSERT ON Submission
FOR EACH ROW
DECLARE
    v_verdict_name VerdictLookup.Name%TYPE;
BEGIN
    SELECT Name INTO v_verdict_name
    FROM VerdictLookup
    WHERE VerdictID = :NEW.VerdictID;

    IF v_verdict_name = 'AC' THEN
        MERGE INTO UserStats us
        USING DUAL
        ON (us.UserID = :NEW.UserID)
        WHEN MATCHED THEN
            UPDATE SET
                TotalSolved = TotalSolved + 1,
                UpdatedAt   = SYSTIMESTAMP
        WHEN NOT MATCHED THEN
            INSERT (UserID, TotalSolved, CurrentRank, UpdatedAt)
            VALUES (:NEW.UserID, 1, NULL, SYSTIMESTAMP);
    END IF;
END;
/

-- Recompute global ranks procedure
CREATE OR REPLACE PROCEDURE sp_recompute_ranks AS
BEGIN
    UPDATE UserStats us
    SET CurrentRank = (
        SELECT RANK() OVER (ORDER BY TotalSolved DESC)
        FROM UserStats us2
        WHERE us2.StatID = us.StatID
    );
    COMMIT;
END;
